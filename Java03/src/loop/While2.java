package loop;

public class While2 {

	public static void main(String[] args) 
	{
		//유사한 코드를 자동 수행	
		//10000번만 수행
		int i=0;
		while(i< 10000)
		{
			System.out.println("안녕");
			i++; //누적연산 ++1,+=1
		}
		//while문 사용시
		//플래그 변수 , 조건식 채우기 , 안에엇 플래ㅡㅋ 값을 증정
		int a = 0;
		while(a < 15)
		{
			System.out.println("반갑습니다");
			a++;
		}
		
	}

}
